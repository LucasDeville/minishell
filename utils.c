#include "minishell.h"

int	ft_has_operator(t_mini *mini)
{
	int		i;
	char	c;

	i = 0;
	while (mini->line[i])
	{
		c = mini->line[i];
		if (c == '|' || (c == '&' && mini->line[i + 1] == '&') || c == '(' 
			|| c == ')' || c == '<' || c == '>')
			return (1);
		i++;
	}
	return (0);
}
